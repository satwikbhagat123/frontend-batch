public class EscapeChar {
    public static void main(String[] args) {
        System.out.println("This is a \"escape\"  program");
        System.out.println("This is a 'escape'   program");
        System.out.println("This is a \tescape  program");
        System.out.println("This is a \n escape  program");
        System.out.println("This is a \\escape\\  program");
    }
}
